static void drawWindow(Turtle turtle)
    {
        faceRelative(turtle, 0, 1);
        turtle.forward(50);
        turtle.turnLeft();
        turtle.forward(50);
        turtle.turnLeft();
        turtle.forward(50);
        turtle.turnLeft();
        turtle.forward(25);
        turtle.turnLeft();
        turtle.forward(50);
        turtle.turnLeft();
        turtle.forward(25);
        turtle.turnLeft();
        turtle.forward(25);
        turtle.turnLeft();
        turtle.forward(50);
        turtle.backward(50);
        turtle.turnRight();
        turtle.forward(25);
        turtle.turnLeft();
        turtle.forward(50);
    }
